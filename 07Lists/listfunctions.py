# the max() function
scores = [90, 23, 87, 54, 21]
largest = max(scores)
print(largest)

animals = ["bear", "dolphin", "zebra", "cow"]
result = max(animals)
print(result)

# the min() function
temps = [92, 38, 73, 29, 82, 9, 38]
smallest = min(temps)
print(smallest)

snacks = ["pizza", "chips", "wings", "burgers"]
smallest = min(snacks)
print(smallest)

# the sum() function
prices = [91, 75, 88, 42, 96, 82, 79, 68, 42]
total = sum(prices)
print(total)

# calculating the average of a list
mean = sum(prices)/len(prices)
print(mean)




