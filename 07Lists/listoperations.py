# the max() function
scores = [29, 13, 84, 92, 31, 90]
highest = max(scores)
print(highest)

animals = ["bear", "dolphin", "zebra", "cow"]
result = max(animals)
print(result)

# the min() function
temps = [56, 27, 96, 87, 8, 67, 16, 87, 41]
lowest = min(temps)
print(lowest)

animals = ["bear", "dolphin", "zebra", "cow"]
result = min(animals)
print(result)

# the sum() function
prices = [62, 76, 98, 78, 92, 79, 87, 89, 62, 76, 81]
total = sum(prices)
print(total)

# calculate the average of the list prices
mean = sum(prices)/len(prices)
print(mean)
