# create a new list with elements in it
fruit = ["strawberry", 58, 3.5]
print(fruit)

# create an empty list
nums = []
print(nums)

# display the individual elements of a list
print(fruit[0])
print(fruit[1])
print(fruit[2])

# change the element 58 to 65
fruit[1] = 65
print(fruit)

# access the last element in a list
print(fruit[-1])

# determining the number of elements in a list
scores = [39, 84, 52, 43, 90, 8]
print(len(scores))

# determining if an element is in a list
drinks = ["tea", "juice", "soda"]
print("coffee" in drinks)
print("soda" in drinks)
