# using the insert() method
breakfast = ["eggs", "juice", "toast"]
breakfast.insert(1, "bacon")
print(breakfast)

scores = [92, 37, 42, 39, 83, 21]
scores.insert(0, 87)
print(scores)

# using the sort() method
expenses = [92, 30, 87, 89, 23, 57]
expenses.sort()
print(expenses)

animals = ["camel", "bear", "goat", "dolphin"]
animals.sort()
print(animals)

# sorting a list from high to low
totals = [29, 38, 71, 32, 90, 84, 17]
totals.sort(reverse=True)
print(totals)

# reversing the order of elements in a list
meals = ["breakfast", "lunch", "dinner"]
meals.reverse()
print(meals)

temps = [21, 90, 38, 41, 29, 39, 72]
temps.reverse()
print(temps)

# removing an element from a list
snacks = ["pizza", "wings", "soda", "chips"]
snacks.remove("soda")
print(snacks)

outcomes = [92, 83, 74, 29, 83, 47, 23]
outcomes.remove(83)
print(outcomes)

