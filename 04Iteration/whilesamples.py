# example of a while loop
x = 0
while x < 5:
    print("hello")
    x = x + 1

# a counter-controlled loop
count = 0
while count < 5:
    print(count)
    count += 1

# summing a sequence of numbers
count = 1
total = 0
while count <= 10:
    total += count
    count += 1
print(total)

# incrementing by a different amount
count = 0
while count < 100:
    print(count)
    count += 10




